# Conditional Statement in Python
# if Statement
a = 35
b = 200
if b > a:
    print("b is greater than a")

# Elif statement
c = 33
d = 32
if c > d:
    print("c is greater than d")
elif c == d:
    print("c and d are equal")

e = 200
f = 33
if e > f:
    print("e is greater than f")
elif e == f:
    print("e and f are equal")
else:
    print("e is greater than f")
