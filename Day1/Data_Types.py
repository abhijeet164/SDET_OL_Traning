# Text Type:	str
a = 'Abhijeet'
b = 'Z'
print(type(a))
print(type(b))

# Numeric Types:	int, float, complex
c = 10
d = 10.5
e = 3 + 2j
print(type(c))
print(type(d))
print(type(e))

# Sequence Types:	list, tuple, range
list1 = [1, 2, 3]
tuple1 = ('Orange', 'Mango', 'Grapes')
f = range(5)
print(type(list1))
print(type(tuple1))
print(type(f))

# Mapping Type:	dict
dict1 = {"name": "John", "age": 36}
print(type(dict1))

# Set Types:	set, frozenset
set1 = {"apple", "banana", "cherry"}
print(type(set1))

# Boolean Type:	bool
g = True
h = False
print(type(g))
print(type(h))
# Binary Types:	bytes, bytearray, memoryview

# None Type:	NoneType
nt1 = None
print(type(nt1))
